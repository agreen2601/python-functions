def run(*kids):
    for kid in kids:
        print(f"{kid} ran like a fool!")

run("Pam", "Sam", "Andrea", "Will")

def swing(*kids):
    for kid in kids:
        print(f"{kid} swung like an idiot!")

swing("Marybeth", "Ariel", "Kevin", "Courtney")

def slide(*kids):
    for kid in kids:
        print(f"{kid} slid like a dingus!")

slide("Mike", "Jack", "Jennifer", "Earl")

def hide(*kids):
    for kid in kids:
        print(f"{kid} hid like a dummy!")

hide("Henry", "Heather", "Hayley", "Hugh")